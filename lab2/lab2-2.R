# 角谷猜想验证程序
# 输入一个正整数n，如果是偶数则除以2，如果是奇数则乘3再加1
# 重复以上过程，直到n = 1，中断循环，并打印“运算成功！”

# 1. 输入一个正整数（你可以修改这里的初始值）
n <- 15 

cat("开始验证角谷猜想，初始值 n =", n, "\n")
cat("变换过程如下：\n")

# 2. 使用 while 循环进行重复运算
# 只要 n 不等于 1，就一直执行循环体内的代码
while (n != 1) {
  if (n %% 2 == 0) { 
    # 如果 n 是偶数（%% 是取余数运算符，余数为0说明是偶数）
    n <- n / 2
  } else {
    # 如果 n 是奇数
    n <- n * 3 + 1
  }
  # 打印出当前这一步变换后的结果
  cat(n, "-> ")
}

# 3. 当 n 变为 1 时，循环中断，打印成功信息
cat("\n运算成功！")