#lab5-6  1997年全国各省市区城市煤气普及率
#煤气普及率数据
gas <- c(94.02,93.10,86.63,66.57,51.68,94.80,61.84,66.32,65.06,
         87.79,94.90,80.29,78.54,57.30,65.70,52.06,73.39,88.53,
         92.05,83.20,43.29,71.16,68.95,54.75,68.42,56.27,71.84,
         39.84,47.27,88.44,85.93)

#(1) 计算各种统计量
m <- mean(gas)          # 均值
v <- var(gas)           # 方差
s <- sd(gas)            # 标准差
Q <- quantile(gas,c(0.25,0.5,0.75))
Q1 <- Q[1]              # 下四分位数
Q2 <- Q[2]              # 中位数
Q3 <- Q[3]              # 上四分位数
M3 <- (Q1 + 2*Q2 + Q3)/4  # 三均值
min_val <- min(gas)     # 最小值
max_val <- max(gas)     # 最大值

# 输出结果
cat("==== 基本统计量 ====\n")
cat("均值 =", m, "\n")
cat("样本方差 =", v, "\n")
cat("样本标准差 =", s, "\n")
cat("下四分位数 Q1 =", Q1, "\n")
cat("中位数 Q2 =", Q2, "\n")
cat("上四分位数 Q3 =", Q3, "\n")
cat("三均值 =", M3, "\n\n")

cat("==== 五数总括 ====\n")
cat("最小值：", min_val, "\n")
cat("下四分位数 Q1：", Q1, "\n")
cat("中位数：", Q2, "\n")
cat("上四分位数 Q3：", Q3, "\n")
cat("最大值：", max_val, "\n\n")

cat("==== 字母值（手动输出）====\n")
cat("M  (中位数)：", Q2, "\n")
cat("F  (第四分位数)：", Q1, Q3, "\n")
cat("E  (第八分位数)：", quantile(gas, c(0.125, 0.875)), "\n")
cat("D  (第十六分位数)：", quantile(gas, c(0.0625, 0.9375)), "\n\n")

#(2) Shapiro-Wilk 正态W检验
cat("==== Shapiro-Wilk 正态W检验 ====\n")
print(shapiro.test(gas))