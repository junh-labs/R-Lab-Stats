#自定义偏度、峰度函数（沿用前面标准公式）
skewness <- function(x){
  n <- length(x)
  xb <- mean(x)
  m3 <- sum((x-xb)^3)/n
  s  <- sd(x)
  m3/s^3
}
kurtosis <- function(x){
  n <- length(x)
  xb <- mean(x)
  m4 <- sum((x-xb)^4)/n
  s  <- sd(x)
  m4/s^4 - 3
}

#(1)均值、方差、标准差、变异系数、偏度、峰度
m <- mean(h)
v <- var(h)
