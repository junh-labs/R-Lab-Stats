#lab5-1
#录入体重数据
w <- c(75.0,64.0,47.4,66.9,62.2,62.2,58.7,63.5,66.6,64.0,57.0,69.0,56.9,50.0,72.0)

#(1)均值
mean_w <- mean(w)
cat("(1)平均体重：",mean_w,"\n")

#(2)方差、标准差（样本方差var，样本标准差sd）
var_w <- var(w)
sd_w <- sd(w)
cat("(2)样本方差：",var_w," 样本标准差：",sd_w,"\n")

#(3)升序排序
sort_w <- sort(w)
cat("(3)升序排序：\n",sort_w,"\n")

#(4)中位数
med_w <- median(w)
cat("(4)中位数：",med_w,"\n")

#(5)指定分位数：0,0.25,0.5,0.75,1
q_w <- quantile(w,probs = c(0,0.25,0.5,0.75,1))
cat("(5)各分位数：\n")
print(q_w)